package pdm;
final public class AttributeData {
  public java.lang.String name;
  public double value;
  public java.lang.String unit;
  public AttributeData() {
  }
  public AttributeData(
    java.lang.String name,
    double value,
    java.lang.String unit
  ) {
    this.name = name;
    this.value = value;
    this.unit = unit;
  }
}
